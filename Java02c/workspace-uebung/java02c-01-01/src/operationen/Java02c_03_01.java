package operationen;

/*  ################################################
Arithmetische Operationen mit verkürztem Quelltext
################################################# */

public class Java02c_03_01 {

	public static void main(String[] args) {
		System.out.print("5 + 9\t\t= ");
		System.out.println(5 + 9);
		
		System.out.print("5 - 9\t\t= ");
		System.out.println(5 - 9);
		
		System.out.print("5 * 9\t\t= ");
		System.out.println(5 * 9);

		System.out.print("9 / 5\t\t= ");
		System.out.print(9 / 5);

		System.out.print(" Rest ");
		System.out.println(9 % 5);

		System.out.print("10 + 8 / 2\t= ");
		System.out.println(10 + 8 / 2);

		//Geänderte Reihenfolge durch Klammern
		System.out.print("(10 + 8) / 2\t= ");
		System.out.println((10 + 8) / 2);
		
	}

}
